CREATE TABLE IF NOT EXISTS public.citycoins (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug text NOT NULL,
  display_name text NOT NULL,
  symbol text NOT NULL,
  created_at timestamp with time zone DEFAULT now() NOT NULL,
  CONSTRAINT citycoins_slug_key UNIQUE (slug)
);

CREATE TABLE IF NOT EXISTS public.app_instances (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug text NOT NULL,
  app_id bigint NOT NULL REFERENCES public.apps(id) ON DELETE CASCADE,
  citycoin_id bigint NOT NULL REFERENCES public.citycoins(id) ON DELETE CASCADE,
  environment text NOT NULL DEFAULT 'development',
  site_url text,
  supabase_project_ref text,
  supabase_url text,
  notes text,
  created_at timestamp with time zone DEFAULT now() NOT NULL,
  CONSTRAINT app_instances_slug_key UNIQUE (slug),
